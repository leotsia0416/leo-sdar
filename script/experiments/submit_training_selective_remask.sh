#!/bin/bash

set -euo pipefail

REPO_ROOT="/work/leotsia0416/projects/SDAR"
LOG_DIR="${REPO_ROOT}/logs"

export TRAIN_PROFILE="${TRAIN_PROFILE:-hard_math}"
export TRAIN_MODEL_PATH="${TRAIN_MODEL_PATH:-${REPO_ROOT}/Models/SDAR-1.7B-Chat-}"
export TRAIN_RUN_NAME_BASE="${TRAIN_RUN_NAME_BASE:-sdar_1p7b_hard_math_gap_selremask_tier1_ent05_rw15}"
export TRAIN_GAP_ROLLOUT_CONFIDENCE_THRESHOLD="${TRAIN_GAP_ROLLOUT_CONFIDENCE_THRESHOLD:-0.95}"
export TRAIN_GAP_REMASK_THRESHOLD="${TRAIN_GAP_REMASK_THRESHOLD:-0.5}"
export TRAIN_GAP_GRPO_REMASK_PENALTY="${TRAIN_GAP_GRPO_REMASK_PENALTY:-0.02}"
export SDAR_GAP_REMASK_THRESHOLD_SCHEDULE="${SDAR_GAP_REMASK_THRESHOLD_SCHEDULE:-0.00:0.30,0.30:0.50,0.70:0.65}"
export SAVE_STEPS="${SAVE_STEPS:-50}"

mkdir -p "${LOG_DIR}"

echo "Submitting training with:" >&2
echo "  TRAIN_PROFILE=${TRAIN_PROFILE}" >&2
echo "  TRAIN_MODEL_PATH=${TRAIN_MODEL_PATH}" >&2
echo "  TRAIN_RUN_NAME_BASE=${TRAIN_RUN_NAME_BASE}" >&2
echo "  TRAIN_GAP_ROLLOUT_CONFIDENCE_THRESHOLD=${TRAIN_GAP_ROLLOUT_CONFIDENCE_THRESHOLD}" >&2
echo "  TRAIN_GAP_REMASK_THRESHOLD=${TRAIN_GAP_REMASK_THRESHOLD}" >&2
echo "  TRAIN_GAP_GRPO_REMASK_PENALTY=${TRAIN_GAP_GRPO_REMASK_PENALTY}" >&2
echo "  SDAR_GAP_REMASK_THRESHOLD_SCHEDULE=${SDAR_GAP_REMASK_THRESHOLD_SCHEDULE}" >&2
echo "  SAVE_STEPS=${SAVE_STEPS}" >&2

sbatch \
    -J SDAR_trsel \
    -o "${LOG_DIR}/train_selective_remask_%j.out" \
    -e "${LOG_DIR}/train_selective_remask_%j.err" \
    --export=ALL \
    "${REPO_ROOT}/script/training.sh"
